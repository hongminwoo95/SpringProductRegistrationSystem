package www.silver.VO;

public class GoodsUpdateVO {
	private String name; // 기존상품명
	private String goodsname; // 상품이름
	private String goodsprice; // 가격
	private String goodscnt; // 수량
	private String goodscomment; // 설명
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGoodsname() {
		return goodsname;
	}
	public void setGoodsname(String goodsname) {
		this.goodsname = goodsname;
	}
	public String getGoodsprice() {
		return goodsprice;
	}
	public void setGoodsprice(String goodsprice) {
		this.goodsprice = goodsprice;
	}
	public String getGoodscnt() {
		return goodscnt;
	}
	public void setGoodscnt(String goodscnt) {
		this.goodscnt = goodscnt;
	}
	public String getGoodscomment() {
		return goodscomment;
	}
	public void setGoodscomment(String goodscomment) {
		this.goodscomment = goodscomment;
	}
	
	
	
}
